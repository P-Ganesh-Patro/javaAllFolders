public class Print10To1 {

    public static void main(String[] args) {
        print10To1();
        
    }
    public static void print10To1(){
        System.out.print("Print 10 to 1 numbers are:- " );
        for(int i = 10; i > 0 ;i--){
            System.out.print(+ i + " ");
        }
        System.out.println();
    }
}